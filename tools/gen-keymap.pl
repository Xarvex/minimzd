#!/bin/perl

use strict;
use warnings;

if (@ARGV != 2) {
    die "Usage: tools/gen-keymap.pl /usr/include/linux/input-event-codes.h third-party/gdk/generated/keynamesprivate.h > generated/keymap.h\n";
}



open IN, $ARGV[0] || die "Cannot open $ARGV[0]: $!\n";

my %keycodes = ();
my %translations = (
    'CTRL' => 'CONTROL',
    'META' => 'SUPER'
);
while (<IN>) {
    next if (!/^#define/);

    /(KEY_((?:KP|FN_)?(?:(LEFT|RIGHT)([^_][\w_]*))?(\4|[\w_]*)))/;
    next if (!defined $1);
    next if ($1 =~ /(?:MIN_INTERESTING|MAX|CNT)$/);

    my $keyname = $2;
    if (defined $3) {
        if (
            $4 eq 'CTRL' ||
            $4 eq 'SHIFT' ||
            $4 eq 'ALT' ||
            $4 eq 'META'
        ) {
            next if (!($3 eq 'LEFT'));
            $keyname = exists $translations{$4} ? $translations{$4} : $4;
        } else {
            $keyname = $4 . $3;
        }
    }

    $keycodes{lc $keyname} = $1;
}

close IN;
undef %translations;



open IN, $ARGV[1] || die "Cannot open $ARGV[1]: $!\n";

my $start = 0;
my %keynames = ();
while (<IN>) {
    if (!$start) {
        next if (!/char\s*keynames\s*\[\s*\]\s*=\s*$/);
        $start = 1;
        next;
    }
    last if /;$/;

    /\"(?:(?:([\w_]*)(?:_(L|R)))|([\w]*))\\/;
    my $keyname = lc(defined $1 ? $1 : $3);
    if (defined $2) {
        next if ($2 eq 'R');
    }
    next if (!exists $keycodes{$keyname});
    $keynames{$keyname} = ();
}

close IN;
undef $start;



my $time = gmtime;
print <<EOF;
/* Generated by gen-keymap.pl at $time
 *
 * Do not edit.
 */

#ifndef MZD_GEN_KEYCODE_H
#define MZD_GEN_KEYCODE_H

#include <linux/uinput.h>

EOF
my @keynames = sort keys %keynames;
my $first;

print '#define MZD_KEYMAP_LEN ' . scalar @keynames . "u\n\n";

print 'static const char *mzd_keymap_names[] = {';
$first = 1;
foreach my $keyname (@keynames) {
    if ($first) {
        $first = 0;
    } else {
        print(',');
    }
    print qq(\n    "$keyname");
}
undef $first;
print "\n};\n\n";

print 'static const unsigned int mzd_keymap_codes[] = {';
$first = 1;
foreach my $keyname (@keynames) {
    if ($first) {
        $first = 0;
    } else {
        print(',');
    }
    print "\n    $keycodes{$keyname}";
}
undef $first;
print "\n};\n\n";

print "#endif";
